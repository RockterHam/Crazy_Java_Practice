package com.rockter.ch4;

public class Switch {

    public void AutoSwitch(){
        System.out.println(3+4+"Helo!");
        System.out.println("Hello!"+3+4);
    }

    public void ForceSwitch(){
        int i = 233;
        byte b = (byte)i;
        System.out.println(b);

        float f = 19.7f;
        int e = (int)f;
        System.out.println(e);
    }

}

